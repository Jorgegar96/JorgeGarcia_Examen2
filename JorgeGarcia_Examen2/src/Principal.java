    private void jb_crearAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_crearAMouseClicked
        if (!tf_nameA.getText().equals("") && !tf_peso.getText().equals("")
                && !this.ta_nacionalidad.getText().equals("") && !this.ta_sueldo.getText().equals("")) {
            try {
                String sexo;
                if (rb_m.isSelected()) {
                    sexo = "M";
                } else {
                    sexo = "F";
                }
                Astronauta a = new Astronauta(
                        tf_nameA.getText(),
                        ta_nacionalidad.getText(),
                        Float.parseFloat(ta_sueldo.getText()),
                        sexo,
                        Float.parseFloat(tf_peso.getText())
                );
                Object[] newrow = {
                    a,
                    a.getSueldo(),
                    a.getNacionalidad(),
                    a.getSexo(),
                    a.getPeso()

                };
                DefaultTableModel model = (DefaultTableModel) jt_A.getModel();
                model.addRow(newrow);
                DefaultComboBoxModel model2 = (DefaultComboBoxModel) cb_astronautas.getModel();
                model2.addElement(a);
                cb_astronautas.setModel(model2);
                jt_A.setModel(model);
                aa.setAstro(a);
                aa.escribirArchivo();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Ocurrio un error!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Hay campos vacios!");
        }
    }//GEN-LAST:event_jb_crearAMouseClicked

