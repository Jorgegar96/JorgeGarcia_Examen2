    private void jb_crearA1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_crearA1MouseClicked
        if (!tf_nameA1.getText().equals("") && !tf_peso1.getText().equals("")
                && !this.ta_nacionalidad1.getText().equals("") && !this.ta_sueldo1.getText().equals("")) {
            try {
                String sexo;
                if (rb_m1.isSelected()) {
                    sexo = "M";
                } else {
                    sexo = "F";
                }
                seleccionado_a.setName(tf_nameA1.getText());
                seleccionado_a.setNacionalidad(ta_nacionalidad1.getText());
                seleccionado_a.setSexo(sexo);
                seleccionado_a.setSueldo(Float.parseFloat(ta_sueldo1.getText()));
                seleccionado_a.setPeso(Float.parseFloat(tf_peso1.getText()));

                DefaultTableModel model2 = (DefaultTableModel) jt_A.getModel();
                for (int i=0 ; i < model2.getRowCount(); i++){
                    model2.removeRow(i);
                }
                for (Astronauta a : aa.getAstronautas()) {
                    Object[] newrow = {
                        seleccionado_a,
                        seleccionado_a.getSueldo(),
                        seleccionado_a.getNacionalidad(),
                        seleccionado_a.getSexo(),
                        seleccionado_a.getPeso()

                    };
                    model2.addRow(newrow);
                }
                jt_A.setModel(model2);
                aa.escribirArchivo();
                JOptionPane.showMessageDialog(this, "Se guardo exitosamente");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Ocurrio un error!");
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Hay campos vacios!");
        }
    }//GEN-LAST:event_jb_crearA1MouseClicked

